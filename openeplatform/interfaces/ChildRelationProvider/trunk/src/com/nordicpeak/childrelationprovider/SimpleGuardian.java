package com.nordicpeak.childrelationprovider;


public class SimpleGuardian extends BasePerson implements Guardian {

	private static final long serialVersionUID = 5461893983752333815L;
	
	public SimpleGuardian() {};

	public SimpleGuardian(String firstname, String lastname, String citizenIdentifier) {
		super();

		this.firstname = firstname;
		this.lastname = lastname;
		this.citizenIdentifier = citizenIdentifier;
	}
}
